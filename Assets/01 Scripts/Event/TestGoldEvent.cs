using System.Collections;
using System.Collections.Generic;
using UnityEngine;

/// <summary>
/// 중요: 임시 테스트용 스크립트입니다.!! 삭제 예정
/// </summary>
public class TestGoldEvent : MonoBehaviour
{
    void Update()
    {
        // 스페이스바를 누르면 방송 시작!
        if (Input.GetKeyDown(KeyCode.Space))
        {
            Debug.Log("골드 수집 명령!");
            GameEventObserver.CollectGold();
        }
    }
}
